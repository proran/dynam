function fmt = lspec_v03(im,ic,il)

mi = {...
    '.',...
'o',...
's',...
'x',...
'^',...
'+',...
'*',...
'd',...
};
% '>',...
% '<',...
% 'v',...
% 'p',...
% 'h',...
li = {...
'-',...
'--',...
':',...
'-.',...
};
ci = {...
    'k',...
    'r',...
    'b',...
    'g',...
    'm',...
    'c',...
};

if ischar(im)==1
    fmt(1) = im;
else
    indx = mod(im,length(mi));
    if indx == 0
        indx = length(mi);
    end
    fmt(1) = mi{indx};
end

if ischar(ic)==1
    fmt(2) = ic;
else
    indx = mod(ic,length(ci));
    if indx == 0
        indx = length(ci);
    end
    fmt(2) = ci{indx};
end

if ischar(il)==1
    fmt = [fmt,il];
else
    indx = mod(il,length(li));
    if indx == 0
        indx = length(li);
    end
    fmt = [fmt,li{indx}];
end